# =====================================================================

HEADER_COLOR = '1F4E79'   # 濃い青
ODD_ROW_COLOR = 'DDEEFF'  # 薄い青
BORDER_COLOR = 'AAAAAA'

CATEGORY_COLORS = {
    '正常系': 'D9EAD3',   # 薄い緑
    '異常系': 'FCE5CD',   # 薄いオレンジ
    '境界値': 'FFF2CC',   # 薄い黄
}

SCREEN_COLORS = {
    'ログイン画面':       'EAF4FB',
    '会員登録画面':       'FEF9E7',
    '宿泊プラン一覧':     'EAFAF1',
    '予約フォーム':       'FDEDEC',
    'マイページ':         'F4ECF7',
}


def make_border():
    thin = Side(style='thin', color=BORDER_COLOR)
    return Border(left=thin, right=thin, top=thin, bottom=thin)


def header_style(cell, text, col_width):
    cell.value = text
    cell.font = Font(bold=True, color='FFFFFF', size=11)
    cell.fill = PatternFill('solid', fgColor=HEADER_COLOR)
    cell.alignment = Alignment(horizontal='center', vertical='center', wrap_text=True)
    cell.border = make_border()


def data_style(cell, value, bg_color='FFFFFF', wrap=True):
    cell.value = value
    cell.fill = PatternFill('solid', fgColor=bg_color)
    cell.alignment = Alignment(horizontal='left', vertical='top', wrap_text=wrap)
    cell.border = make_border()
    cell.font = Font(size=10)


def generate():
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = 'テストケース一覧'

    # ─── ヘッダー行 ───
    col_widths = [14, 10, 16, 36, 28, 52, 40, 8]
    for col_idx, (header, width) in enumerate(zip(HEADERS, col_widths), start=1):
        cell = ws.cell(row=1, column=col_idx)
        header_style(cell, header, width)
        ws.column_dimensions[get_column_letter(col_idx)].width = width

    ws.row_dimensions[1].height = 22

    # ─── データ行 ───
    current_screen = None
    for row_idx, tc in enumerate(TEST_CASES, start=2):
        tc_id, category, screen, name, precondition, steps, expected, auto = tc

        # 画面が変わるときに区切り行を入れる（簡易グループ）
        bg = SCREEN_COLORS.get(screen, 'FFFFFF')
        cat_bg = CATEGORY_COLORS.get(category, 'FFFFFF')

        values = [tc_id, category, screen, name, precondition, steps, expected, auto]
        bg_colors = [bg, cat_bg, bg, bg, bg, bg, bg, bg]

        for col_idx, (val, cell_bg) in enumerate(zip(values, bg_colors), start=1):
            cell = ws.cell(row=row_idx, column=col_idx)
            data_style(cell, val, cell_bg)

        ws.row_dimensions[row_idx].height = max(
            60,
            steps.count('\n') * 14 + 20,
            expected.count('\n') * 14 + 20,
        )

    # ─── ヘッダー行を固定 ───
    ws.freeze_panes = 'A2'

    # ─── 表紙シート ───
    ws_cover = wb.create_sheet('表紙', 0)
    ws_cover['B2'] = 'Hotel Planisphere'
    ws_cover['B2'].font = Font(bold=True, size=20, color=HEADER_COLOR)
    ws_cover['B3'] = 'テストケース仕様書'
    ws_cover['B3'].font = Font(bold=True, size=14)
    ws_cover['B5'] = f'作成日: {date.today().isoformat()}'
    ws_cover['B6'] = f'テストケース総数: {len(TEST_CASES)} 件'
    ws_cover['B7'] = 'テスト対象: https://hotel-example-site.takeyaqa.dev/ja/'
    ws_cover.column_dimensions['B'].width = 60

    # 画面別集計
    from collections import Counter
    screen_counts = Counter(tc[2] for tc in TEST_CASES)
    ws_cover['B9'] = '【画面別テストケース数】'
    ws_cover['B9'].font = Font(bold=True)
    for i, (screen, count) in enumerate(screen_counts.items(), start=10):
        ws_cover.cell(row=i, column=2).value = f'  {screen}: {count} 件'

    output = 'hotel_planisphere_testcases.xlsx'
    wb.save(output)
    print(f'[OK] {output} を生成しました ({len(TEST_CASES)} テストケース)')


if __name__ == '__main__':
    generate()
